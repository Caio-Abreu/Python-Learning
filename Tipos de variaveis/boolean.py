ativo = True
print(ativo)

desativo = False
print(desativo)

# Negação (not)
print(not desativo)

# Ou (or)
# True or True = True
# True or False = True
# False or False = False
# False or True = True

print(ativo or desativo)

# E (and)
# True and True = True
# True and False = False
# False and True = False
# False and False = False